import { Radio } from "../radio";

import {
  hasProperties,
  makeNodeFactory,
  makeShallowWrapper,
  testForwardRefAsExoticComponentIntegration,
  testTransformHelpersIntegration,
} from "../../../__tests__/testing";

const COMPONENT = Radio;
const COMPONENT_NAME = "Radio";
const DEFAULT_ELEMENT = "input";
const BULMA_CLASS_NAME = undefined;

const makeNode = makeNodeFactory(COMPONENT);

describe(`${COMPONENT_NAME} component`, () => {
  hasProperties(COMPONENT, {
    defaultProps: { as: DEFAULT_ELEMENT },
  });

  testForwardRefAsExoticComponentIntegration(
    makeNode,
    makeShallowWrapper,
    DEFAULT_ELEMENT,
    BULMA_CLASS_NAME,
  );

  testTransformHelpersIntegration(makeNode, makeShallowWrapper);

  it("should be a radio", () => {
    const node = makeNode({});
    const wrapper = makeShallowWrapper(node);
    expect(wrapper.props().type).toEqual("radio");
  });
});
